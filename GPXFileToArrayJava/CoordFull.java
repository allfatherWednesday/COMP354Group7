
public class CoordFull {
	double lat;
	double lon;
	String time;
	double ele;
	
	public CoordFull(double lat, double lon, String time, double ele) {
		super();
		this.lat = lat;
		this.lon = lon;
		this.time = time;
		this.ele = ele;
	}

	public double getLat() {
		return lat;
	}


	public double getLon() {
		return lon;
	}

	public String getTime() {
		return time;
	}

	public double getEle() {
		return ele;
	}


	
	
}
